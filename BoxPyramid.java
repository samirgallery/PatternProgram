import java.lang.*;
import java.util.*;;

public class BoxPyramid {
    public static void main(String[] args) {
        // Scanner scan = new Scanner(System.in);
        int j, i;
        for (i = 0; i <= 5; i++) {
            for (j = 0; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }

}
